#include <stdio.h>

int ft_sqrt(int nb)
{
	long	i;

	i = (nb / 2) - 1;
	if (nb <= 0)
		return (0);
	while(i * i != nb && i * i > nb / 2)
	{
		i--;
	}
	if (i * i <= nb / 2)
		return (0);
	return (i);
}
